/**
 * Generate a complete SaaS project scaffold in the workspace.
 * Returns an array of relative paths written, or empty array if workspace missing.
 */
export function generateProjectScaffold(session: AutopilotSession): string[] {
  const dir = workspaceDir(session.sessionId);
  if (!fs.existsSync(dir)) return [];

  const projectDir = path.join(dir, "project");
  ensureDir(projectDir);

  const ctx = buildScaffoldContext(session);
  const writtenPaths: string[] = [];

  for (const file of SCAFFOLD_FILES) {
    const fullPath = path.join(dir, file.path);

    // Prevent path traversal
    const resolved = path.resolve(fullPath);
    if (!resolved.startsWith(path.resolve(dir))) continue;

    const content = file.content(ctx);
    safeWriteFile(fullPath, content);
    writtenPaths.push(file.path);
  }

  // Auto-validate the generated scaffold
  validateGeneratedProject(session.sessionId);

  return writtenPaths;
}

/**
 * Check whether a project scaffold already exists in the workspace.
 */
export function projectScaffoldExists(sessionId: string): boolean {
  const dir = workspaceDir(sessionId);
  const pkgPath = path.join(dir, "project", "package.json");
  return fs.existsSync(pkgPath);
}

