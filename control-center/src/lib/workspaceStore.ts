function formatLogEntries(logs: AutopilotLog[]): string {
  return logs
    .slice(0, 60)
    .map((log) => `[${formatTimestamp(log.timestamp)}] [${log.level.toUpperCase()}] [${log.agent}] ${log.message}`)
    .join("\n");
}
