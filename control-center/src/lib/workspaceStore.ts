/**
 * Generate or update agent artifact files in the workspace.
 * Returns an array of relative paths for the files that were written.
 */
export function generateAgentArtifact(session: AutopilotSession, task: AutopilotTask): string[] {
  const config = AGENT_ARTIFACTS[task.agent];
  if (!config) return [];

  const dir = workspaceDir(session.sessionId);
  if (!fs.existsSync(dir)) return [];

  const artifactDir = path.join(dir, config.dir);
  ensureDir(artifactDir);

  const writtenPaths: string[] = [];

  for (const file of config.files) {
    const relPath = `${config.dir}/${file.name}`;
    const fullPath = path.join(dir, relPath);

    // Prevent path traversal
    const resolved = path.resolve(fullPath);
    if (!resolved.startsWith(path.resolve(dir))) continue;

    const content = file.sections(session, task).join("\n");
    safeWriteFile(fullPath, content);
    writtenPaths.push(relPath);
  }

  return writtenPaths;
}

